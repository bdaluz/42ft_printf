char	*ft_strrchr(const char *s, int c)
{
	char	*ch;

	ch = 0;
	while (*s)
	{
		if (*s == (char)c)
			ch = (char *)s;
		s++;
	}
	if (*s == (char)c)
		ch = (char *)s;
	return (ch);
}
